#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tab;
	size_t	i;

	tab = (char *)s;
	i = 0;
	if (n != 0)
	{
		while (i < n)
		{
			tab[i] = 0;
			i++;
		}
	}
}
